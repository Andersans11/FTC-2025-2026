package org.firstinspires.ftc.teamcode.RobotStuff.Config.Subconfigs.Presets;

public class DepositArmPreset {
    double DepositArmPivot;
    double DepositClawPivot;
    double DepositClawServo;

    public DepositArmPreset(double DepositArmPivot, double DepositClawPivot, double DepositClawServo) {
        this.DepositArmPivot = DepositArmPivot;
        this.DepositClawPivot = DepositClawPivot;
        this.DepositClawServo = DepositClawServo;
    }
}
